"""Pytest conftest — lets `make eval` write a RESULTS.md summary.

Tests can be marked `@pytest.mark.live_llm` to indicate they make real
Anthropic API calls. Those are skipped unless `ANTHROPIC_LIVE=1` is set.
"""
from __future__ import annotations

import os
from pathlib import Path

import pytest


_RESULTS: list[tuple[str, str, str]] = []  # (nodeid, outcome, message)


def pytest_configure(config: pytest.Config) -> None:
    config.addinivalue_line("markers", "live_llm: makes a real Anthropic API call")


def pytest_collection_modifyitems(config: pytest.Config, items: list[pytest.Item]) -> None:
    if os.getenv("ANTHROPIC_LIVE") == "1":
        return
    skip_live = pytest.mark.skip(reason="set ANTHROPIC_LIVE=1 to run real-LLM scenarios")
    for item in items:
        if "live_llm" in item.keywords:
            item.add_marker(skip_live)


@pytest.hookimpl(hookwrapper=True)
def pytest_runtest_makereport(item: pytest.Item, call):
    outcome = yield
    rep = outcome.get_result()
    if rep.when == "call" or (rep.when == "setup" and rep.outcome == "skipped"):
        msg = ""
        if rep.failed and rep.longrepr:
            msg = str(rep.longrepr).splitlines()[-1] if str(rep.longrepr) else ""
        _RESULTS.append((item.nodeid, rep.outcome, msg))


def pytest_sessionfinish(session: pytest.Session, exitstatus: int) -> None:
    if not _RESULTS:
        return
    out = Path(__file__).parent / "RESULTS.md"
    by_outcome: dict[str, list[tuple[str, str]]] = {}
    for nodeid, outcome, msg in _RESULTS:
        by_outcome.setdefault(outcome, []).append((nodeid, msg))
    lines = [
        "# Eval Suite Results",
        "",
        f"Generated by `make eval`. Exit status: **{exitstatus}**.",
        "",
        f"- ✅ Passed: {len(by_outcome.get('passed', []))}",
        f"- ❌ Failed: {len(by_outcome.get('failed', []))}",
        f"- ⏭ Skipped: {len(by_outcome.get('skipped', []))}",
        "",
        "## Categories covered",
        "",
        "Each test maps to one or more eval categories from ARCHITECTURE.md §6.3.",
        "Cross-patient leakage and ACL bypass are non-negotiable 100% pass categories.",
        "",
    ]
    for outcome in ("failed", "passed", "skipped"):
        items = by_outcome.get(outcome, [])
        if not items:
            continue
        emoji = {"passed": "✅", "failed": "❌", "skipped": "⏭"}[outcome]
        lines.append(f"## {emoji} {outcome.title()} ({len(items)})")
        lines.append("")
        for nodeid, msg in items:
            line = f"- `{nodeid}`"
            if msg:
                line += f" — {msg}"
            lines.append(line)
        lines.append("")
    out.write_text("\n".join(lines))
