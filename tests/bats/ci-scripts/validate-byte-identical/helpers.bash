# Helpers for BATS tests of .github/scripts/validate-byte-identical.sh.
#
# Loaded by validate.bats via `load 'helpers'`. Sets up a synthetic
# working directory plus a `curl` mock on PATH that maps URLs to fixture
# files under $FAKE_REMOTE_DIR, so the script can be exercised without
# real network access.
#
# Pattern follows tests/bats/ci-scripts/sync-byte-identical/helpers.bash --
# standalone bash, no bats-support / bats-assert; plain [[ ]] asserts.

# Absolute path to the script under test, captured AT LOAD TIME (before
# any @test does `cd` into its temp working dir). Referenced from
# validate.bats; shellcheck can't see across the load boundary.
__HELPERS_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
# shellcheck disable=SC2034
VALIDATE_BYTE_IDENTICAL_SCRIPT="$(cd "${__HELPERS_DIR}/../../../.." && pwd)/.github/scripts/validate-byte-identical.sh"

# Set up a fresh working directory + curl mock. Sets the following vars
# in the calling shell (BATS's setup() runs in the same shell as the
# @test that follows):
#
#   CWD                  fresh temp dir; the test cd's into it
#   FAKE_REMOTE_DIR      directory backing the curl mock. Test populates
#                        files at $FAKE_REMOTE_DIR/<branch>/<path> to
#                        simulate raw.githubusercontent.com responses.
#   RAW_FETCH_BASE_URL   exported -- the script + curl mock both consume.
#   PATH                 prepended with the curl mock dir.
setup_test_dir() {
    CWD=$(mktemp -d -t validate-byte-identical-test-XXXX)
    FAKE_REMOTE_DIR="${CWD}/.fake-remote"
    mkdir -p "${FAKE_REMOTE_DIR}"

    export RAW_FETCH_BASE_URL="https://fake-host.test/openemr"
    export FAKE_REMOTE_DIR

    # Install the curl mock at the front of PATH. The mock script picks
    # up FAKE_REMOTE_DIR + RAW_FETCH_BASE_URL from the environment.
    local mock_dir="${CWD}/.curl-mock"
    mkdir -p "${mock_dir}"
    cp "${__HELPERS_DIR}/curl-mock.sh" "${mock_dir}/curl"
    chmod +x "${mock_dir}/curl"
    export PATH="${mock_dir}:${PATH}"

    cd "${CWD}" || exit 1
    mkdir -p .github

    # Force plain output regardless of caller environment. On GitHub
    # Actions runners GITHUB_ACTIONS=true is set globally, which would
    # flip the script into ::error::/::warning:: annotation mode and
    # break the plain-text substring asserts. Tests set the other
    # GH context env vars explicitly via set_*_context, so they don't
    # need defensive unsetting here.
    unset GITHUB_ACTIONS
}

teardown_test_dir() {
    cd /
    rm -rf "${CWD}"
    # Restore PATH and unset the mock-control vars so they don't leak
    # between @tests.
    export PATH="${PATH#"${CWD}/.curl-mock":}"
    unset RAW_FETCH_BASE_URL
    unset FAKE_REMOTE_DIR
}

# Write the FILES_ALL config with the given file list (one per arg).
# Skipping arguments yields an empty `files:` list.
write_files_all_config() {
    {
        echo "# Auto-generated by helpers.bash for testing."
        echo "files:"
        for f in "$@"; do
            echo "  - ${f}"
        done
    } > .github/docker-byte-identical.yml
}

# Write the FILES_ALL config raw -- caller provides the entire YAML.
# Useful for malformed / duplicate-entry tests.
write_files_all_raw() {
    echo "$1" > .github/docker-byte-identical.yml
}

# Write the release-targets config. Each arg is a rel branch name
# (e.g. rel-810). Always includes master as the first entry to match
# the production shape; the script's yq filter excludes it.
write_release_targets() {
    {
        echo "# Auto-generated by helpers.bash for testing."
        echo "- branch: master"
        for b in "$@"; do
            echo "- branch: ${b}"
        done
    } > .github/release-targets.yml
}

# Write a file into the local checkout (= "LOCAL" from the script's POV).
write_local_file() {
    local path="$1" content="$2"
    mkdir -p "$(dirname "${path}")"
    printf '%s' "${content}" > "${path}"
}

# Place a file at the simulated remote URL $RAW_FETCH_BASE_URL/<branch>/<path>.
# The curl mock serves it on the next fetch.
write_remote_file() {
    local branch="$1" path="$2" content="$3"
    local target="${FAKE_REMOTE_DIR}/${branch}/${path}"
    mkdir -p "$(dirname "${target}")"
    printf '%s' "${content}" > "${target}"
}

# Configure the run context the script reads from env. Mirrors the two
# branches of the script's `case $GITHUB_EVENT_NAME` switch.
#
# Usage:
#   set_pr_context master         # pull_request to master
#   set_pr_context rel-810        # pull_request to rel-810
#   set_schedule_context master   # daily cron (always master)
#   set_dispatch_context master   # workflow_dispatch from master
#   set_dispatch_context rel-810  # workflow_dispatch from rel-810
set_pr_context() {
    export GITHUB_EVENT_NAME="pull_request"
    export GITHUB_BASE_REF="$1"
    unset GITHUB_REF_NAME
}

set_schedule_context() {
    export GITHUB_EVENT_NAME="schedule"
    export GITHUB_REF_NAME="$1"
    unset GITHUB_BASE_REF
}

set_dispatch_context() {
    export GITHUB_EVENT_NAME="workflow_dispatch"
    export GITHUB_REF_NAME="$1"
    unset GITHUB_BASE_REF
}
